let addNumber = (num1, num2) => {
  return num1 + num2;
};
let substractNumber = (num1, num2) => {
  return num1 - num2;
};
let multiplyNumber = (num1, num2) => {
  return num1 * num2;
};

let divideNumber = (num1, num2) => {
  return num1 / num2;
};
export { addNumber, substractNumber, multiplyNumber, divideNumber };
